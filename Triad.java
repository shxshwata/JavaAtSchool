public class Triad
{
    public static void main()
    {
        int a,b,c,d,e,f,g,h,j,k,l;
        for (int i=100;i<=999;i++)
        {
            a=i;
            b=i*2;
            c=i*3;
            c=a/100;
            d=a/10-(c*10);
            e=a%10;
            f=b/100;
            g=b/10-(f*10);
            h=b%10;
            j=c/100;
            k=c/10-(j*10);
            l=c%10;
            
        }
    }
}